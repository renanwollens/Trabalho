package acessoAosDados;

public class Aluno {
	private int id;
	private String nome;
	private Float nota1;
	private Float nota2;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public Float getNota1() {
		return nota1;
	}
	public void setNota1(Float nota1) {
		this.nota1 = nota1;
	}
	public Float getNota2() {
		return nota2;
	}
	public void setNota2(Float nota2) {
		this.nota2 = nota2;
	}	
}
